## Python ##
import os.path;
## Pygame ##
import pygame;


################################################################################
## Global vars                                                                ##
################################################################################
_assets_search_path = ".";


################################################################################
## Search Path Functions                                                      ##
################################################################################
def set_search_path(path):
    global _assets_search_path;
    _assets_search_path = path;

def get_search_path():
    return _assets_search_path;


################################################################################
## Image Functions                                                            ##
################################################################################
def load_image(name):
    return pygame.image.load(os.path.join(get_search_path(), name));


################################################################################
## Font Functions                                                             ##
################################################################################
def load_font(name, size):
    return pygame.font.Font(os.path.join(get_search_path(), name), size);


